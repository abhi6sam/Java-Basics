/* Program to find Unicode of #,$,%,6 */

class Unicodes {
    public static void main(String[] args) {
	    char a =  '#';
		char b = '$';
		char c = '%';
		char d = '6';
		
		System.out.println((int) a);
		System.out.println((int) b);
		System.out.println((int) c);
		System.out.println((int) d);
		
	}
}